fun main() {
    val a: Int = 42
    val b: Long = 123456789L
    val c: Short = -78
    val d: Byte = 127
    val e: Float = 3.14F
    val f: Double = 2.7182818284
    val g: Char = 'A'
    val h: Boolean = true
    val i: String = "Hello, Kotlin!"
    println("Integer: $a")
    println("Long: $b")
    println("Short: $c")
    println("Byte: $d")
    println("Float: $e")
    println("Double: $f")
    println("Char: $g")
    println("Boolean: $h")
    println("String: $i")
}